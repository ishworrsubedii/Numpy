
import numpy as np
# ***********Mean*******************
first_array=np.array([1,2,3,8])
mean_result=np.mean(first_array) 
print(mean_result)  # Output 3.5


# ***********Max*******************
first_array = np.array([1, 2, 3, 8])
max_result = np.max(first_array)
print(max_result) #Output 8


# ***********Linespace*******************
linspace=np.linspace(1,6,num=6)
print(linspace)  # Output [1. 2. 3. 4. 5. 6.]



